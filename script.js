const openProjectModal = (project, options = {}) => {
  const { mode = "root", restoreScrollTop = 0 } = options;
  const modal = document.getElementById("project-modal");
  const body = document.getElementById("project-modal-body");
  if (mode === "root") {
    projectModalHistory = [];
  } else if (
    mode === "inline" &&
    activeProjectContext &&
    activeProjectContext.title !== project.title
  ) {
    projectModalHistory.push({
      title: activeProjectContext.title,
      scrollTop: body.scrollTop,
    });
  }

  const caseStudy = buildProjectCaseStudy(project);
  const showBack = projectModalHistory.length > 0;

  body.innerHTML = `
    ${
      showBack
        ? `<button class="project-modal-back" type="button" id="project-modal-back">Back to previous project</button>`
        : ""
    }
    <p class="section-kicker">Project Case Study</p>
    <h3 class="project-modal-title">${caseStudy.title}</h3>
    <p class="project-modal-subtitle">${caseStudy.subtitle}</p>
    ${
      caseStudy.paperUrl
        ? `<p class="project-paper-link-wrap"><a class="project-paper-link" href="${caseStudy.paperUrl}" target="_blank" rel="noreferrer">${caseStudy.paperLabel || "Open paper"}</a></p>`
        : ""
    }
    ${caseStudy.sections
      .map((section) => {
        const paragraphs = (section.paragraphs || [])
          .map((paragraph) => `<p>${paragraph}</p>`)
          .join("");
        const bullets = (section.bullets || []).length
          ? `<ul>${section.bullets.map((bullet) => `<li>${bullet}</li>`).join("")}</ul>`
          : "";

        return `
          <section class="project-detail-section">
            <h4>${section.heading}</h4>
            ${paragraphs}
            ${bullets}
          </section>
        `;
      })
      .join("")}
  `;

  activeProjectContext = { title: project.title };

  if (showBack) {
    const backButton = document.getElementById("project-modal-back");
    backButton.addEventListener("click", () => {
      const previous = projectModalHistory.pop();
      if (!previous) {
        return;
      }
      const previousProject = content.projects.find((item) => item.title === previous.title);
      if (previousProject) {
        openProjectModal(previousProject, {
          mode: "back",
          restoreScrollTop: previous.scrollTop || 0,
        });
      }
    });
  }

  body.querySelectorAll(".inline-project-link").forEach((link) => {
    link.addEventListener("click", (event) => {
      const targetTitle = link.dataset.projectTitle;
      if (!targetTitle) {
        return;
      }
      event.preventDefault();
      if (activeProjectContext?.title === targetTitle) {
        return;
      }
      const targetProject = content.projects.find((item) => item.title === targetTitle);
      if (targetProject) {
        openProjectModal(targetProject, { mode: "inline" });
      }
    });
  });

  modal.classList.remove("hidden");
  modal.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
  requestAnimationFrame(() => {
    body.scrollTop = restoreScrollTop;
  });
};

