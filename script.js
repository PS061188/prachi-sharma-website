const setupRevealOnScroll = () => {
  const items = document.querySelectorAll(".reveal-on-scroll");
  if (!items.length) {
    return;
  }

  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion) {
    items.forEach((item) => item.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries, activeObserver) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          activeObserver.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -8% 0px",
    }
  );

  items.forEach((item) => observer.observe(item));
};

const setupHeroHeadlineTyping = () => {
  const headline = document.getElementById("hero-headline");
  const hero = document.getElementById("hero");
  if (!headline || !hero) {
    return;
  }

  const fullText = content.heroHeadline;
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion) {
    headline.textContent = fullText;
    return;
  }

  let typingTimeout = null;
  let hasStarted = false;

  const typeHeadline = () => {
    if (typingTimeout) {
      window.clearTimeout(typingTimeout);
    }
    hasStarted = true;
    headline.textContent = "";
    let index = 0;

    const step = () => {
      headline.textContent = fullText.slice(0, index + 1);
      index += 1;
      if (index < fullText.length) {
        typingTimeout = window.setTimeout(step, index < 8 ? 78 : 54);
      }
    };

    step();
  };

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting && !hasStarted) {
          typeHeadline();
        } else if (!entry.isIntersecting && entry.boundingClientRect.top < 0) {
          hasStarted = false;
        }
      });
    },
    {
      threshold: 0.52,
    }
  );

  observer.observe(hero);
};

